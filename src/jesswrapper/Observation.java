package jesswrapper;

public class Observation {
	private String name;
	private String value;
	
	public Observation(String name, String value) {
		super();
		this.name = name;
		this.value = value;
	}

	public String getName() {
		return name;
	}

	public String getValue() {
		return value;
	}
	
	
}
